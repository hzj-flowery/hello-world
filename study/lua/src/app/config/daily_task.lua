-- metatable
local mt = { 
    __index = function(t, k) 
        assert(__key_map[k], "cannot find " .. k .. " in daily_task")
        return t._raw[__key_map[k]]
    end
}

-- 
function daily_task.length()
    return #daily_task._data
end

-- 
function daily_task.hasKey(k)
    if __key_map[k] == nil then
        return false
    else
        return true
    end
end

--
function daily_task.indexOf(index)
    if index == nil or not daily_task._data[index] then
        return nil
    end

    return setmetatable({_raw = daily_task._data[index]}, mt)
end

--
function daily_task.get(id)
    
    return daily_task.indexOf(__index_id[id])
        
end

--
function daily_task.set(id, tkey, nvalue)
    local record = daily_task.get(id)
    if record then
        local keyIndex = __key_map[tkey]
        if keyIndex then
            record._raw[keyIndex] = nvalue
        end
    end
end

--
function daily_task.index()
    return __index_id
end

