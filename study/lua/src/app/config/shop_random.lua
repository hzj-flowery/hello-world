    __index = function(t, k) 
        assert(__key_map[k], "cannot find " .. k .. " in shop_random")
        return t._raw[__key_map[k]]
    end
}

-- 
function shop_random.length()
    return #shop_random._data
end

-- 
function shop_random.hasKey(k)
    if __key_map[k] == nil then
        return false
    else
        return true
    end
end

--
function shop_random.indexOf(index)
    if index == nil or not shop_random._data[index] then
        return nil
    end

    return setmetatable({_raw = shop_random._data[index]}, mt)
end

--
function shop_random.get(id)
    
    return shop_random.indexOf(__index_id[id])
        
end

