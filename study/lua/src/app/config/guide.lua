function guide.length()
    return #guide._data
end

-- 
function guide.hasKey(k)
    if __key_map[k] == nil then
        return false
    else
        return true
    end
end

--
function guide.indexOf(index)
    if index == nil or not guide._data[index] then
        return nil
    end

    return setmetatable({_raw = guide._data[index]}, mt)
end

--
function guide.get(id)
    
    return guide.indexOf(__index_id[id])
        
end

--
function guide.set(id, key, value)
    local record = guide.get(id)
    if record then
        local keyIndex = __key_map[key]
        if keyIndex then
            record._raw[keyIndex] = value
        end
    end
end

--
function guide.index()
    return __index_id
end

