end

--
function pvpsingle_auction.set(id, key, value)
    local record = pvpsingle_auction.get(id)
    if record then
        local keyIndex = __key_map[key]
        if keyIndex then
            record._raw[keyIndex] = value
        end
    end
end

--
function pvpsingle_auction.index()
