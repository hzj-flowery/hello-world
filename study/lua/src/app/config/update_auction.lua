    return #update_auction._data
end

-- 
function update_auction.hasKey(k)
    if __key_map[k] == nil then
        return false
    else
        return true
    end
end

--
function update_auction.indexOf(index)
    if index == nil or not update_auction._data[index] then
        return nil
    end

    return setmetatable({_raw = update_auction._data[index]}, mt)
end

--
function update_auction.get(id)
    
    return update_auction.indexOf(__index_id[id])
        
end

